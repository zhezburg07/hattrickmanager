// "Опыт построения" — насколько команда обжилась в текущей расстановке.
// Демонстрационное значение по той же усечённой словесной шкале, что и
// форма игрока (0-8, "отлично" → "отсутствует", без топ-уровней вроде
// "легендарно"/"божественно" — для построений такая куцая шкала обычно и используется):
// распространённые схемы получают высокий опыт, нестандартные сочетания слотов — низкий.
const commonFormations = new Set(["4-4-2", "4-3-3", "3-5-2", "4-5-1", "5-3-2", "3-4-3"]);

export function formationExperienceLevel(formationLabel: string, fieldPlayerCount: number): number | null {
  if (fieldPlayerCount < 10) return null; // состав ещё не укомплектован — опыта нет
  return commonFormations.has(formationLabel) ? 8 : 3;
}

export const formationExperienceHint =
  "Опыт построения растёт по мере того, как команда играет в этой расстановке. " +
  "Низкий опыт повышает риск растерянности игроков во время матча.";
